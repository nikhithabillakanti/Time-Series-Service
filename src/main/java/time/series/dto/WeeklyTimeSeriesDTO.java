package time.series.dto;

public class WeeklyTimeSeriesDTO {
	private int week;
	private double total;

	public int getWeek() {
		return week;
	}

	public void setWeek(int week) {
		this.week = week;
	}

	public double getTotal() {
		return total;
	}

	public void setTotal(double total) {
		this.total = total;
	}
}
